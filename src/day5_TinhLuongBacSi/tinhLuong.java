package day5_TinhLuongBacSi;

public class tinhLuong {
	public double tongLuong(int luongcoban, int heSoLuong) {
		return luongcoban*heSoLuong;
	}
	public double tongLuong(int luongcoban, int heSoLuong, double namKN) {
		return luongcoban*heSoLuong*namKN;
	}
	public double tongLuong(int luongcoban, int heSoLuong, double namKN, double loai) {
		return luongcoban*heSoLuong*namKN*loai;
	}
	
}
